package fr.univ.blois.siad.m1.javaee.jpa.ex03;


public class Category {

    private Long id;

    private String title;

    public Category() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
